extension UIColor {
    class func fumcRedColor() -> UIColor {
        return UIColor(red: 132/255, green: 21/255, blue: 33/255, alpha: 1)
    }
    class func fumcMagentaColor() -> UIColor {
        return UIColor(red: 190/255, green: 64/255, blue: 127/255, alpha: 1)
    }
}